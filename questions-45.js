(function applyAnswerKey() {
  const letterToIndex = { A: 0, B: 1, C: 2, D: 3 };
  const questions = Array.isArray(window.AIRTIS_QUESTIONS) ? window.AIRTIS_QUESTIONS : [];
  const key = window.AIRTIS_ANSWER_KEY || {};

  for (let i = 0; i < questions.length; i++) {
    const qNo = i + 1;
    const letter = String(key[qNo] || "").trim().toUpperCase();
    const idx = letterToIndex[letter];
    if (!Number.isInteger(idx)) continue;
    questions[i].c = idx;
  }
})();
